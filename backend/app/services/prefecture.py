def find_nearest_prefecture(lat: float, lon: float) -> str:
    """緯度経度から最寄りの都道府県コードを返す.

    日本国内の座標同士の比較なので、ユークリッド距離で十分。
    """
    best_code = "13"  # fallback: 東京都
    best_dist = float("inf")

    for code, (_, plat, plon) in PREFECTURES.items():
        dist = math.hypot(lat - plat, lon - plon)
        if dist < best_dist:
            best_dist = dist
            best_code = code

    return best_code


def calc_weather_severity(
    chance_of_rain: int,
    precip_mm: float,
    wind_kph: float,
    condition: str,
) -> int:
    """天気の悪さスコア (0-100) を計算する。大きいほど天気が悪い."""
    score = 0
    # 降水確率: 最大40点
    score += int(chance_of_rain * 0.4)
    # 降水量: 最大30点
    score += min(int(precip_mm * 3), 30)
    # 風速: 最大15点
    score += min(int(wind_kph / 4), 15)
    # 天気条件キーワード: 最大15点
    bad_keywords = {
        "Snow": 15, "Blizzard": 15, "snow": 15, "雪": 15,
        "Thunder": 12, "thunder": 12, "雷": 12,
        "Heavy rain": 12, "Torrential": 12, "大雨": 12,
        "Sleet": 10, "sleet": 10, "みぞれ": 10,
        "Rain": 8, "rain": 8, "Drizzle": 5, "雨": 8,
        "Fog": 3, "Mist": 3, "霧": 3,
    }
    keyword_score = 0
    for keyword, points in bad_keywords.items():
        if keyword in condition:
            keyword_score = max(keyword_score, points)
    score += keyword_score

    return min(score, 100)
